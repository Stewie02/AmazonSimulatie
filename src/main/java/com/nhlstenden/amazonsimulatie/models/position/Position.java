package com.nhlstenden.amazonsimulatie.models.position;

public interface Position {

    /**
     * Returns the value of the X-coordinate
     * @return X-coordinate
     */
    double getX();

    /**
     * Returns the value of the Y-coordinate
     * @return Y-coordinate
     */
    double getY();

    /**
     * Returns the value of the Z-coordinate
     * @return Z-coordinate
     */
    double getZ();

}
